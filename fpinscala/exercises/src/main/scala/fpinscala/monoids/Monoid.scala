    def parse(str: String): WC = {
      val firstIndex = str.indexOf(' ')
      val lastIndex = str.lastIndexOf(' ')
      if (str.indexOf(' ') < 0) Stub(str)
      else if (firstIndex == lastIndex) Part(str.substring(0, firstIndex), 0, str.substring(lastIndex + 1, str.length))
      else {
        val (l, r) = s.splitAt(s.length / 2)
        wcMonoid.op(parse(l), parse(r))
